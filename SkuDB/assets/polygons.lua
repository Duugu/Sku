SkuPrintMT = {
   __tostring = function(thisTable)
      local tStr = ""
      local function tf(ttable, tTab)
         for k, v in pairs(ttable) do
            if type(v) == "table" then
               if tonumber(k) then
                  tStr = tStr..tTab.."["..k.."] = {".."\n"
               else
                  tStr = tStr..tTab..""..k.." = {".."\n"
               end
               tf(v, tTab.."  ")
               tStr = tStr..tTab.."},".."\n"
            elseif type(v) == "boolean" then
               tStr = tStr..tTab..k.." = "..tostring(v)..",".."\n"
            else
               tStr = tStr..tTab..k.." = "..v..",".."\n"
            end
         end
      end
      tf(thisTable, "")
      return tStr
   end,
}
